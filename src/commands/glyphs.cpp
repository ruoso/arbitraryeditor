// editor.cameras.contact_sheet — the embedded ASCII bitmap glyph table (D-sheet-4, A21).
//
// A headless L1 job has no font: §8 makes `views`/`dock` "the ONLY layer that sees
// ImGui", so ImGui's atlas is structurally unreachable from `commands`, and A20's
// ledger says the editor vendors exactly ONE encode dependency — which stays true
// only if captions cost no second external. So the caption face is DATA: 95 cells
// covering U+0020..U+007E, a 5x7 glyph in a 6x8 box, one byte per row with the
// glyph's five columns in the LOW FIVE BITS, leftmost column = bit 4. 760 bytes,
// compile-time constant, no rasterizer, no `assets/` directory to ship and locate at
// runtime, and no second entry in A20's one-vendored-encode-dependency ledger.
//
// This TU is pure data — no branches, no arcs — so gcov attributes nothing to it.
// `text_width` / `fit_text` / `draw_text` (the code that READS the table) live in
// contact_sheet.cpp, where they are covered by tests/contact_sheet_test.cpp.

